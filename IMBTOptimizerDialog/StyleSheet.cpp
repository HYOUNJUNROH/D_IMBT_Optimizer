bool CStyleSheet::initialize()
{
	//////////////////////////////////////////////////////////////////////////
	// Main View
	//////////////////////////////////////////////////////////////////////////
	{ 
		//////////////////////////////////////////////////////////////////////////
		// Button Color
		//////////////////////////////////////////////////////////////////////////
		m_colorSignInButton.setColor(RGB(144,238,144), RGB(99,184,255), RGB(99,184,255)); // Light Green

		//////////////////////////////////////////////////////////////////////////
		// Button Color
		//////////////////////////////////////////////////////////////////////////
		//m_colorMainViewButton.setColor(RGB(50,61,72), RGB(42,51,60), RGB(42,51,60));
		m_colorMainViewButton.setColor(RGB(220, 220, 220), RGB(99, 184, 255), RGB(99, 184, 255));

		//////////////////////////////////////////////////////////////////////////
		// Button Font
		//////////////////////////////////////////////////////////////////////////
		createCFont(&m_cfontMainViewButton, 15, FW_SEMIBOLD, false, FONT_NAME);

		//m_rgbMainViewSubject = RGB(16,200,224);
		m_rgbMainViewSubject = RGB(255,255,255);
		m_rgbMainViewBottom = RGB(255,255,255);
		m_rgbMainViewUserName = RGB(246,150,75);
	}

	
	//////////////////////////////////////////////////////////////////////////
	// Menu View
	//////////////////////////////////////////////////////////////////////////
	{ 
		//////////////////////////////////////////////////////////////////////////
		// Button Color
		//////////////////////////////////////////////////////////////////////////
		m_colorMenuViewButton.setColor(RGB(220,220,220), RGB(99,184,255), RGB(99,184,255));

		//////////////////////////////////////////////////////////////////////////
		// Button Font
		//////////////////////////////////////////////////////////////////////////
		createCFont(&m_cfontMenuViewButton, 15, FW_SEMIBOLD, false, FONT_NAME);
	}


	//////////////////////////////////////////////////////////////////////////
	// Task View
	//////////////////////////////////////////////////////////////////////////
	{ 
		//////////////////////////////////////////////////////////////////////////
		// Button Color
		//////////////////////////////////////////////////////////////////////////
		m_colorTaskViewButton.setColor(RGB(176,196,222), RGB(99,184,255), RGB(99,184,255));

		//////////////////////////////////////////////////////////////////////////
		// Button Font
		//////////////////////////////////////////////////////////////////////////
		createCFont(&m_cfontTaskViewButton, 15, FW_SEMIBOLD, false, FONT_NAME);

		//////////////////////////////////////////////////////////////////////////
		// Title Font
		//////////////////////////////////////////////////////////////////////////
		createCFont(&m_cfontTaskViewTitleLabel, 20, FW_BOLD, false, FONT_NAME);

		//////////////////////////////////////////////////////////////////////////
		// Title Color
		//////////////////////////////////////////////////////////////////////////
		m_rgbTaskViewTitle = RGB(75, 160, 246);

		//////////////////////////////////////////////////////////////////////////
		// Label Font
		//////////////////////////////////////////////////////////////////////////
		createCFont(&m_cfontTaskViewLabel, 14, FW_BOLD, false, FONT_NAME);
	}

	//////////////////////////////////////////////////////////////////////////
	// Surgery View
	//////////////////////////////////////////////////////////////////////////
	{ 
		createCFont(&m_cfontSurgeryViewLable, 15, FW_BOLD, false, FONT_NAME);
	}
	
	return true;
}


void CStyleSheet::createCFont(CFont *pCFont, int nHeight, int nWeight, BYTE bItalic, LPCTSTR lpszFacename)
{
	pCFont->CreateFont(nHeight, 0, 0, 0, nWeight, bItalic, false, false, 
					   ANSI_CHARSET, OUT_DEFAULT_PRECIS, CLIP_DEFAULT_PRECIS, DEFAULT_QUALITY, VARIABLE_PITCH|FF_MODERN, 
					   lpszFacename);
}